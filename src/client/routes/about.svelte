  <title>About the Ruinous Twitch Addons</title>
</svelte:head>

<div class="prose md:prose-lg lg:prose-xl max-w-none">
  <p>
    <strong>Ruinous Addons</strong> are a set of Twitch add-ons that Twitch
    streamers can add to their channels without any coding required. Largely, they
    are games and other fun things that <a class="underline" href="https://twitter.com/OdatNurd">@OdatNurd</a>
    has created on his <a class="underline" href="https://twitch.tv/odatnurd">Twitch channel</a>. They're
    freely available and require a minimal set of permissions on your Twitch Channel, and may also require
    you to add a <em>Browser Source</em> to your stream setup.
  </p>
  <p>
    To get started, <a class="underline" href="/login">Login</a> using your Twitch account, then browse the
    list of addons and add them to your channel.
  </p>
</div>
